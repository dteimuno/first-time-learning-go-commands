// if you do not assign a value to a variable, the value is considered zero
//Data Types: bool int float64 string
/* Examples:
bool = false
int = 0
float64 = 0.0
string = ""
*/

//zero values int
package main
import "fmt"
func main() {
  var i int
  fmt.Printf("%d", i)
}
// value printed will be zero

//zero values float
package main
import "fmt"
func main() {
  var fl float64
  fmt.Printf("%d", fl)
}
